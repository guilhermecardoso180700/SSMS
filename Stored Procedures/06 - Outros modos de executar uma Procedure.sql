CREATE PROCEDURE BUSCA_NOTAS_CLIENTE
@CPF AS VARCHAR(12),
@DATA_INICIAL AS DATE = '20160101',
@DATA_FINAL AS DATE = '20161231'
AS
BEGIN 
SELECT * FROM [NOTAS FISCAIS] WHERE 
CPF = @CPF AND
DATA >= @DATA_INICIAL AND
DATA <= @DATA_FINAL
END



SELECT * FROM [NOTAS FISCAIS]
SELECT * FROM [TABELA DE CLIENTES]

-- Executando Procedure
EXEC BUSCA_NOTAS_CLIENTE @CPF = '19290992743', @DATA_INICIAL = '20161201', @DATA_FINAL = '20161201'

-- Executando Procedure, inserindo variáveis na ordem que foram declaradas
EXEC BUSCA_NOTAS_CLIENTE '19290992743', '20161201', '20161201'

-- Executando Procedure, utilizando o valor padrão em uma das variáveis:
EXEC BUSCA_NOTAS_CLIENTE '19290992743', DEFAULT, '20161201'